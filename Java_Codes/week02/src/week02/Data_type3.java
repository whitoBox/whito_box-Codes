package week02;

public class Data_type3 {
	public static void main(String[] args) {
		byte    bData = -128; //byte -128 ~ 127
		char    cData = 90; //char only 양수
		float   fData = 3.14f; //3.14 = double(x) / 3.14 = float(o) 
		long    lData = 10000000000L; // 'L' 넣어줘야 long 타입으로 지정된다.
		boolean boolD = true;
		
		System.out.println("bData = " + bData);
		System.out.println("cData = " + cData);
		System.out.println("fData = " + fData);
		System.out.println("lData = " + lData);
		System.out.println("boolD = " + boolD);
	}
}
